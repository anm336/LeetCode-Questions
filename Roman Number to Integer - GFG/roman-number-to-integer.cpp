class Solution {
  public:
    int romanToDecimal(string &str) {
        // code here
        int ans = 0;
        unordered_map<char, int> m = {{'I', 1}, {'V', 5}, {'X', 10}, {'L', 50}, {'C', 100}, {'D', 500}, {'M', 1000}};
        
        int n = str.size();
        for(int i=0;i<n;i++){
            int temp = m[str[i]];
            if(i!=n-1){
                if(temp >= m[str[i+1]]){
                    ans+=temp;
                }
                else{
                    ans-=temp;
                }
            }
            else{
                ans+=temp;
            }
        }
        
        return ans;
    }
};

